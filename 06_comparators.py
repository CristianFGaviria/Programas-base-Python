# Operadores de comparacion

# > mayor que
print(7 > 3)
print(3 > 7)
print(7 > 7)
# < menor que
print(5 < 6)
print(6 < 5)
print(6 < 6)
# >= mayor e igual que
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)
# <= menor e igual que
print(2 <= 1)
print(2 <= 1)
print(2 <= 2)
# == igualacion
print(2 == 2)
print(2 == 3)
# ! Diferente de
print(2 != 2)
print(2 != 3)

print("Apple" == 'Apple')
print("Apple" == 'apple')

